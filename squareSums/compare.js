const sameArray = (a, b) => a.every((x, i) => x === b[i]);

const sortUniqueStrip = (arrOfPaths) => {
    const sorted = arrOfPaths.map(x => x[0] < x[x.length - 1] ? x.reverse().join(',') : x.join(','))
        .sort().map(x => x.split(','));    
    var uniqueSols = [sorted[0], ...sorted.slice(1).map((x, i) => sameArray(x, sorted[i]) ? false : x)]
        .filter(Boolean)
    var stripped = uniqueSols.map(x => x.map(n => n>25 ? '..' : n));
    return stripped
}


